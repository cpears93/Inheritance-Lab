package staff;

public abstract class Employee {

    private String name;
    private int niNumber;
    private int salary;

//  constructor

    public String getName() {
        return name;
    }

    public int getNiNumber() {
        return niNumber;
    }

    public int getSalary() {
        return salary;
    }

    public int raiseSalary(){
        return raiseSalary();
    }

    public int payBonus(){
        return payBonus();
    }
}
